const friends = ["balam", "kalam", "salam"];
console.log(friends);

// Removes first element of array
friends.shift();
console.log(friends);

// Removes first element of array
friends.shift();
console.log(friends);

// Add back 2nd element of original array
friends.unshift("kalam");
console.log(friends);

// Add back 1st element of original array
friends.unshift("balam");
console.log(friends);
