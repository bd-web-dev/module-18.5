// 4. Checking if it's an Array
// Instructions:

// Create different variables, each containing either an array or a non-array value.

// Now use isArray to check if each variable is an array.

// Print a message to the console indicating whether each variable is an array or not.

const numberValue = 23;
console.log("Is numberValue an array: " + Array.isArray(numberValue));
const stringValue = "string";
console.log("Is stringValue an array: " + Array.isArray(stringValue));
const booleanValue = true;
console.log("Is booleanValue an array: " + Array.isArray(booleanValue));
const arrayExample = ["1", "2", "3"];
console.log("Is arrayExample an array: " + Array.isArray(arrayExample));
